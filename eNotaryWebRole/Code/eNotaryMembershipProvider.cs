        public override bool ValidateUser(string username, string password)
        {
            bool validation;
           
            try
            {
                 // create new user for this person
                        MD5 md5 = new MD5CryptoServiceProvider();
                        Byte[] originalBytes = ASCIIEncoding.Default.GetBytes(password);
                        Byte[] encodedBytes = md5.ComputeHash(originalBytes);
                // verify if users exist in bd
                string pswd = BitConverter.ToString(encodedBytes);
           
                if (_db.Users.Where(u=>u.Username == username && u.PsswdEncrypt ==pswd ).Count()>0)
                    validation = true;
                else
                    validation = false;
            }
            catch (Exception ex)
            {
                validation = false;
            }
            finally
            {
            }

            return validation;
        }

    


